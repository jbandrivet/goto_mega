    # ──────────────────────────────────────────────────────────
    # PROJECTION STÉRÉOGRAPHIQUE (comme Stellarium)
    # Centre de vue = (self._caz, self._calt) en degrés
    # FOV = demi-angle du champ en degrés
    # ──────────────────────────────────────────────────────────
    def _proj(self, az, alt):
        """(az,alt) degrés → (px,py) canvas. None si hors champ."""
        W = self.winfo_width()  or 800
        H = self.winfo_height() or 600
        cx, cy = W/2, H/2
        # Rayon du cercle = demi-petit côté × 0.93
        R = min(W, H) / 2 * 0.93

        # Conversion en vecteurs 3D (repère horizontal)
        # x = Est, y = Nord, z = Zénith
        def sph2xyz(az_d, alt_d):
            a = math.radians(az_d)
            e = math.radians(alt_d)
            return (-math.sin(a)*math.cos(e),   # x (Est = +sin az)
                     math.cos(a)*math.cos(e),   # y (Nord)
                     math.sin(e))               # z (zénith)

        xc,yc,zc = sph2xyz(self._caz, self._calt)
        xs,ys,zs = sph2xyz(az, alt)

        # Produit scalaire → cos(séparation)
        dot = xc*xs + yc*ys + zc*zs
        dot = max(-1.0, min(1.0, dot))
        if dot < -0.1: return None   # derrière

        # Projection stéréographique : r = 2*R * tan(θ/2)
        theta = math.acos(dot)
        fov_r = math.radians(self._fov)
        if theta > fov_r * 1.05: return None

        if theta < 1e-8:
            return cx, cy

        # Projection sur le plan tangent au centre de vue
        # Vecteur tangent Est (perpendiculaire à zc dans le plan horizontal-Est)
        # e_x = Nord × Centre / |Nord × Centre|
        # On construit la base locale du plan image
        # ux = vecteur "droite" sur l'écran (Est dans le ciel)
        # uy = vecteur "haut" sur l'écran (Nord dans le ciel)
        # Vraie base locale pour le plan tangent :
        # ux = normalize(centre × zenith) (Est)
        # uy = normalize(ux × centre) (Nord)
        
        # ux = centre × zenith (0, 0, 1)
        uxr = yc*1.0 - zc*0.0
        uyr = zc*0.0 - xc*1.0
        uzr = xc*0.0 - yc*0.0
        nm = math.sqrt(uxr**2+uyr**2+uzr**2)
        if nm < 1e-9:
            # Centre au zénith : axe x = Est
            uxr,uyr,uzr = 1,0,0
            nm=1
        uxr/=nm; uyr/=nm; uzr/=nm
        
        # uy = ux × centre
        ux2 = uyr*zc - uzr*yc
        uy2 = uzr*xc - uxr*zc
        uz2 = uxr*yc - uyr*xc
        nm2 = math.sqrt(ux2**2+uy2**2+uz2**2)
        if nm2 < 1e-9: ux2,uy2,uz2 = 0,-1,0; nm2=1
        ux2/=nm2; uy2/=nm2; uz2/=nm2

        # Composantes de l'étoile dans la base locale
        dx = uxr*xs + uyr*ys + uzr*zs
        dy = ux2*xs + uy2*ys + uz2*zs

        # Distance projetée (stéréo)
        r_stereo = 2 * math.tan(theta/2) / (2 * math.tan(fov_r/2)) * R

        nm3 = math.sqrt(dx**2+dy**2)
        if nm3 < 1e-9: return cx, cy

        # dx → droite à l'écran, dy → haut à l'écran (canvas y inversé)
        return cx + r_stereo*dx/nm3, cy - r_stereo*dy/nm3

    def _unproj(self, px, py):
        """(px,py) canvas → (az,alt) degrés ou (None,None)."""
        W=self.winfo_width() or 800; H=self.winfo_height() or 600
        R=min(W,H)/2*0.93
        ndx=(px-W/2)/R; ndy=-(py-H/2)/R
        r=math.sqrt(ndx**2+ndy**2)
        if r>1.0: return None,None

        fov_r=math.radians(self._fov)
        theta = 2*math.atan(r * math.tan(fov_r/2))

        # Base locale (même calcul que _proj)
        def sph2xyz(az_d,alt_d):
            a=math.radians(az_d); e=math.radians(alt_d)
            return (-math.sin(a)*math.cos(e), math.cos(a)*math.cos(e), math.sin(e))

        xc,yc,zc = sph2xyz(self._caz,self._calt)
        uxr = yc; uyr = -xc; uzr = 0.0
        nm=math.sqrt(uxr**2+uyr**2+uzr**2)
        if nm<1e-9: uxr,uyr,uzr=1,0,0; nm=1
        uxr/=nm; uyr/=nm
        ux2=uyr*zc-uzr*yc; uy2=uzr*xc-uxr*zc; uz2=uxr*yc-uyr*xc
        nm2=math.sqrt(ux2**2+uy2**2+uz2**2)
        if nm2<1e-9: ux2,uy2,uz2=0,-1,0; nm2=1
        ux2/=nm2; uy2/=nm2; uz2/=nm2

        nm3=max(r,1e-9)
        dx2=ndx/nm3; dy2=ndy/nm3

        # Vecteur étoile = cos(θ)*centre + sin(θ)*(dx2*ux + dy2*uy)
        xs=math.cos(theta)*xc + math.sin(theta)*(dx2*uxr + dy2*ux2)
        ys=math.cos(theta)*yc + math.sin(theta)*(dx2*uyr + dy2*uy2)
        zs=math.cos(theta)*zc + math.sin(theta)*(dx2*uzr + dy2*uz2)

        alt=math.degrees(math.asin(max(-1,min(1,zs))))
        az=math.degrees(math.atan2(-xs,ys))%360
        return az,alt

