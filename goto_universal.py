    def _wheel(self, e):
        zoom_in = (e.num == 4 or e.delta > 0)
        self._fov = max(5, self._fov * (0.82 if zoom_in else 1.22))
        self._fov = min(90, self._fov)
        self.cfg.set("skymap_fov_deg", self._fov)
        self.redraw()

    def _press(self, e):
        self._drag0 = (e.x, e.y)
        self._dc0   = (self._caz, self._calt)

    def _drag(self, e):
        if not self._drag0: return
        dx = e.x - self._drag0[0]
        dy = e.y - self._drag0[1]
        W = self.winfo_width()  or 800
        H = self.winfo_height() or 600
        R = min(W,H)/2*0.93
        # Sensibilité : FOV / R degrés par pixel
        sc = self._fov / R
        self._caz  = (self._dc0[0] - dx * sc) % 360
        self._calt = max(1.0, min(89.0, self._dc0[1] + dy * sc))
        self.redraw()

    def _release(self, e):
        if not self._drag0: return
        dx = abs(e.x - self._drag0[0])
        dy = abs(e.y - self._drag0[1])
        self._drag0 = None
        if dx < 5 and dy < 5:
            self._on_click(e)

    def _on_click(self, e):
        """Détecte le clic sur un objet DSO ou dans le ciel."""
        best = None; bd = 20
        for obj in self._dso:
            az, alt = Astro.eq_to_horiz(obj["ra"], obj["dec"], self._lst, self._lat)
            p = self._proj(az, alt)
            if not p: continue
            d = math.hypot(p[0]-e.x, p[1]-e.y)
            if d < bd: bd = d; best = obj
        if best and self.on_obj_click:
            self.on_obj_click(best)
        elif self.on_sky_click:
            az, alt = self._unproj(e.x, e.y)
            if az is not None:
                ra, dec = Astro.horiz_to_eq(az, alt, self._lst, self._lat)
                self.on_sky_click(az, alt, ra, dec)

    def _motion(self, e):
        """Tooltip au survol d'un objet."""
        if self._tip:
            self.delete(self._tip); self._tip = None
        for obj in self._dso:
            az, alt = Astro.eq_to_horiz(obj["ra"], obj["dec"], self._lst, self._lat)
            p = self._proj(az, alt)
            if not p: continue
            if math.hypot(p[0]-e.x, p[1]-e.y) < 14:
                nm = TYPE_NAMES.get(obj["type"], obj["type"])
                self._tip = self.create_text(
                    e.x+14, e.y-12,
                    text=f"  {obj['name']}   m{obj['mag']:.1f}   {nm}  ",
                    fill="#00ccff", font=("Helvetica",8),
                    anchor="w")
                # Fond pour lisibilité
                bb = self.bbox(self._tip)
                if bb:
                    bg = self.create_rectangle(bb[0]-2,bb[1]-1,bb[2]+2,bb[3]+1,
                                               fill="#040a10",outline="#0a2030")
                    self.tag_lower(bg, self._tip)
                break

